        <h3><a name="faq"></a>Settling in: FAQs</h3>
        <h5><strong>Why should I keep my cat in one room?</strong></h5>
        <p>This helps them feel safe, secure and lets them&nbsp;establish their own territory. It’s easier for toilet&nbsp;training and cleaning.</p>
        <h5><strong>Why is my new cat hiding?</strong></h5>
        <p>Don’t be concerned. Cats find moving house and&nbsp;meeting new people stressful. Allow them time to&nbsp;settle in, and wait for them to come to you instead&nbsp;of forcing contact. Talk gently to them and sit nearby&nbsp;so they can relax. Let your cat approach you when&nbsp;they feel ready.</p>
        <h5><strong>When do I let my cat explore outdoors?</strong></h5>
        <p>After 3-4 weeks for cats or 6-8 weeks for kittens&nbsp;(or once your cat has settled), introduce your cat to the&nbsp;garden in short supervised periods. Gradually get them&nbsp;used to this bigger territory before giving them free&nbsp;range. Kittens should be supervised outside to protect&nbsp;them against other cats, dogs or potential threats.&nbsp;It’s best to keep cats inside at night to avoid&nbsp;injury or fights.</p>
