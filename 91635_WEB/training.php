        <h1>Animal training</h1>
        <h4><a name="top"></a><i>Behavioural advice and training for the cats:</h4>

        <p>Cats cannot be physically forced or&nbsp;manipulated to do what you want.&nbsp;Cats are not pack animals and don’t&nbsp;respond to punishment.</p>

        <p>Never smack or swat a cat, shake them or rub&nbsp;their nose in their urine or faeces if they toilet&nbsp;inappropriately. This is cruel, and will only teach&nbsp;your cat to avoid doing this around you. Your cat&nbsp;will become stressed and scared of you, which&nbsp;will make the problem worse.</p>
        </i>
        <h5><strong>Teach your cat good behaviour</strong></h5>
        <p>Regularly talking to your cat helps to&nbsp;establish a bond and good behaviour. Teach&nbsp;your cat to do the right thing and reward them&nbsp;for good behaviour. For example, teach them&nbsp;to use a scratching post by dragging a string up&nbsp;the side of the post for the cat to follow.&nbsp;Reward and praise profusely.</p>

        <h5><strong>Confine your cat while training</strong></h5>
        <p>It is best to confine your cat to one quiet&nbsp;room until trained. If you leave the cat alone to&nbsp;roam your house they may develop unwanted&nbsp;toilet and scratching habits.</p>

        <h5><strong>Teach basic commands</strong></h5>
        <p>Many cats can be trained using treats and other&nbsp;positive encouragement. You can also teach your&nbsp;cat to respond to simple commands&nbsp;such as ‘no’.</p>

        <h5><strong>Biting and scratching</strong></h5>
        <p>Cats rarely bite or get rough out of anger; it’s&nbsp;usually out of fear. You need to eliminate the cause&nbsp;of the fear. Be patient, and don’t force your cat&nbsp;into cuddles. Contact an animal behaviourist&nbsp;if the problem persists.</p>
        <p>Kittens often bite for sheer playfulness. Never use&nbsp;your hands for playing. If your kitten gets rough,&nbsp;correct this the way their mother would. Utter a high&nbsp;pitched yelp – this will make your kitten freeze and&nbsp;you can then pull your hand away and immediately&nbsp;stop playing. Don’t resume play for at least three&nbsp;minutes. Sometimes cats gives warning signs before&nbsp;play biting such as a twitch of the tail, a look in their&nbsp;eye or position of the head. Always try to end contact&nbsp;before the play bite. Click here for more&nbsp;information about cat aggression.</p>

        <h5><strong>Scratching furniture</strong></h5>
        <p>Scratching is natural to sharpen cats’ claws. It is also&nbsp;good for scent-marking and exercising.</p>
        <p>It’s easy to get a cat to use a scratch post by&nbsp;placing the scratch pole near where they want to&nbsp;scratch.</p>
        <p>Drag a string up&nbsp;the side of the post for your cat to follow, or wait&nbsp;until your cat is close to the post and&nbsp;scratch with your nails. The sound will&nbsp;encourage your cat.</p>

        <h5><strong>Other undesirable behaviour</strong></h5>
        <p>Other behaviour such as chewing and digging up&nbsp;pot plants, or pushing objects off tables is usually&nbsp;caused by boredom. It is common in indoor cats&nbsp;that don’t get a lot of stimulation or exercise,&nbsp;and is easier to prevent than to correct.</p>
        <p>You can help by playing with your cat at least&nbsp;twice a day, using different toys to get them&nbsp;running, leaping and pouncing to the point of&nbsp;exhaustion. Also leave some independent toys&nbsp;(such as table tennis balls) out for your cat to&nbsp;amuse themselves during the day.</p>
        <p>Get two&nbsp;kittens instead of one, to keep each other&nbsp;company and provide play opportunities.</p>

        <h5><strong>Spraying</strong></h5>
        <p>Spraying is usually done by non-desexed cats but&nbsp;sometimes desexed cats also spray to mark territory.&nbsp;The cat will direct a small amount of urine onto&nbsp;objects such as trees or walls.</p>
        <p>Your cat should have no need to spray indoors as the&nbsp;house is their accepted den. Spraying might happen&nbsp;if your cat feels insecure or threatened, e.g with the&nbsp;arrival of a new pet, new human or in a multi-cat&nbsp;household. Sometimes an increased challenge from&nbsp;a cat outdoors can start the problem. Cats may spray&nbsp;door mats if your shoes have brought in the scent of&nbsp;a strange cat.</p>
        <p>If spraying occurs, clean, eliminate causes and&nbsp;retrain your cat. If your cat sprays due to an&nbsp;outside threat, such as the neighbour’s cat, board&nbsp;up cat flaps to reassure your cat the house is safe.</p>
        <p>A repeat or problematic sprayer can be confined to&nbsp;one room, preferably a warm room where they can&nbsp;sleep next to a source of heat, such as a radiator. The&nbsp;cat will probably feel secure in this room and so will&nbsp;not spray. Use of a Feliway spray or diffuser may&nbsp;help. If spraying ceases, your cat can be allowed into&nbsp;other rooms gradually, under supervision. Click here&nbsp;for more enrichment ideas.</p>
        <p>If spraying continues, speak to your vet.</p>
