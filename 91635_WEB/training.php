        <h3><a name="arrivinghome"></a>Essential information on arriving home</h3>
        <h5><strong>Set up one room</strong></h5>
        <ul style="list-style-type: disc;">
            <li>Keep your cat in one room for 2-3 days.&nbsp;A quiet room is ideal.</li>
            <li>Ensure the room is secure and well-ventilated.</li>
        </ul>
        <h5><strong>Make the room ‘cat comfy’</strong></h5>
        <ul style="list-style-type: disc;">
            <li>Set up the room with water, food, toys, litter&nbsp;tray, scratching post and somewhere to hide.</li>
            <li>Provide a bed or comfy blanket to help your cat settle.</li>
        </ul>
        <h5><strong>Litter tray position</strong></h5>
        <ul style="list-style-type: disc;">
            <li>Put the litter tray in a private area at least&nbsp;1.5m away from food and bedding.</li>
        </ul>
        <h5><strong>Cat proofing</strong></h5>
        <ul style="list-style-type: disc;">
            <li>Remove dangerous wires, curtain cords,&nbsp;or items your cat can chew or&nbsp;get tangled in.</li>
            <li>Remove breakable or scratchable items.</li>
            <li>Keep the toilet lid closed.</li>
        </ul>
        <h5><strong>Try not to scare your new cat</strong></h5>
        <ul style="list-style-type: disc;">
            <li>Ask family members to try to keep quiet&nbsp;to avoid scaring your cat.</li>
            <li>Don’t worry if your cat hides for a&nbsp;few days. This is quite normal behaviour.</li>
        </ul>
        <h5><strong>Explore the house slowly</strong></h5>
        <ul style="list-style-type: disc;">
            <li>After 2-3 days in one room, slowly introduce&nbsp;your cat to the rest of the house.</li>
            <li>Do this room by room to avoid&nbsp;overwhelming them.</li>
        </ul>
        <h5><strong>Keep inside</strong></h5>
         <ul style="list-style-type: disc;">
            <li>Keep your cat inside so&nbsp;they don’t get lost or run away.</li>
            <li>Adult cats – minimum 3-4 weeks.</li>
            <li>Kittens – 6-8 weeks. Kittens should be&nbsp;supervised outside until they are old/big&nbsp;enough to protect themselves.</li>
            <li>Keep all doors and windows closed.</li>
        </ul>
